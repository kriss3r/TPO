package inputAndAlgorythms;

public class Algorythms {
	public static double frstAlg(double input){
		double output= input/2;
		return output;
		
	}
	public static double scndAlg(double input){
		double output= input*2;
		return output;
		
	}
	public static double thrdAlg(double input){
		double output= input+500;
		return output;
		
	}
	public static double revFrstAlg(double input){
		double output= input*2;
		return output;
		
	}
	public static double revScndAlg(double input){
		double output= input/2;
		return output;
		
	}
	public static double revThrdAlg(double input){
		double output= input-500;
		return output;
		
	}
}
